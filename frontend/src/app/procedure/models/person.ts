export class Person {
    id: number;
    name: string;
    identityCode: number;
    email: string;
  
    constructor(id: number, name: string, identityCode: number, email: string) {
      this.id = id;
      this.name = name;
      this.identityCode = identityCode;
      this.email = email;
    }
  }
  